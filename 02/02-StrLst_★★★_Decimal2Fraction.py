import math

# 一行输入，形如：7,,0  或  0,08,3  或  123,456,789
A, B, C = [x.strip() for x in input().split(',')]

m = len(B)
n = len(C)

# 拼接成整数（空串用 '0' 占位，int('') 不合法）
X1 = int((A + B + C) or '0')
X0 = int((A + B) or '0')

num = X1 - X0
den = 10**(m + n) - 10**m

g = math.gcd(num, den)     # 约分
num //= g
den //= g

print(f"{num} / {den}")
